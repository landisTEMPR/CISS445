# Arithmetic and mixed types
print(1 + 3.5)
print(2 ** 8)
print(10 / 3)
print(10 % 3)
print(True + 1)

# Variables
a = 10
b = 20
c = a + b
print(c)

# Boolean operators
print(True and False)
print(True or False)
print(not True)

# If / else
x = 0
if x == 0:
    print(True)
else:
    print(False)

# Nested if
a = 5
b = 0
if a > 0:
    if b == 0:
        print(a)
    else:
        print(b)

# While loop
i = 0
s = 0
while i < 5:
    s = s + i
    i = i + 1
print(s)
print(i)
